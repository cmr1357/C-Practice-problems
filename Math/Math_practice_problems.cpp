#include<iostream>
#include<climits>
#include<cmath>// for using log, squareroot..etc we need to use math library.

using namespace std;
	
int main()
{
	// To find the maximum and minimum
	
	cout<<max(5,10)<<'\n';
	cout<<min(5,10)<<'\n';
	
	// To find squareroot, logarithm, round
	
	cout<<sqrt(64)<<'\n';
	cout<<round(3.14)<<'\n';
	cout<<log(2)<<'\n';	
	
	return 0;
}
