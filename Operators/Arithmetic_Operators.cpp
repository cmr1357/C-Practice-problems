//Arithmetic Operators

#include<iostream>
#include<climits>

using namespace std;

int main()
{
	// Addition;
	int a=10,b=20;
	cout<<"sum = a + b= "<<a+b<<'\n';
	
	//Subtraction
	
	cout<<"Difference = a- b = "<<a-b<<'\n';
	
	//Multiplication
	
	cout<<"product = a * b = "<<a*b<<'\n';
	
	//Division
	
	cout<<"Quotient = a / b = "<<a/b<<'\n';
	
	//Modulus operation
	
	cout<<"Remainder = a % b = "<<a%b<<'\n';
	
	//Increment
	
	cout<<"increment = a++ = "<<a++ <<'\n';
	
	//Decrement
	
	cout<<"increment = a-- = "<<a--<<'\n';
	
	return 0;
}
